package kr.co.Dto;

public class EplDTO {
	String name;
	int game;
	int win;
	int draw;
	int lose;
	int goal;
	int ngoal;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGame() {
		return game;
	}
	public void setGame(int game) {
		this.game = game;
	}
	public int getWin() {
		return win;
	}
	public void setWin(int win) {
		this.win = win;
	}
	public int getDraw() {
		return draw;
	}
	public void setDraw(int draw) {
		this.draw = draw;
	}
	public int getLose() {
		return lose;
	}
	public void setLose(int lose) {
		this.lose = lose;
	}
	public int getGoal() {
		return goal;
	}
	public void setGoal(int goal) {
		this.goal = goal;
	}
	public int getNgoal() {
		return ngoal;
	}
	public void setNgoal(int ngoal) {
		this.ngoal = ngoal;
	}
	
	
	
}
